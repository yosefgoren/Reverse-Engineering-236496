template <size_t ArrSize>
int count_occur(uint8_t value, const array<pair<uint8_t, uint8_t>, ArrSize>& arr){
	int count = 0;
	for(auto& [in, out] : arr){
		if(in == value){
			count++;
		}
	}
	return count;
}


